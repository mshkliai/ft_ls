#include "list_ls.h"

int	main(int ac, char **av)
{
	int		i;

	i = 0;
	if (ac > 1)
		parse_flags(av, &i, g_flags);
	if (!i)
		i = 1;
	if (!av[i])
		ls_end(0);
	else
		ls_end(av + i);
	return (0);
}
